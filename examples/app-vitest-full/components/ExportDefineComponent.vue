<script lang="ts">
export default defineComponent({
  props: {
    myProp: {
      type: String,
      required: true,
    },
    myArrayProp: {
      type: Array as PropType<string[]>,
      default: () => ([]),
    },
    myObjProp: {
      type: Object as PropType<{ title: string }>,
      default: () => ({}),
    },
  },
  setup(props) {
    return {
      setupMyProp: 'X' + props.myProp,
    }
  },
})
</script>
